//Q1: Write a program to input two numbers and display their sum.//
#include<stdio.h>

int main()
{
    int a,b;
    printf("Enter two Numbers");
    scanf("%d %d",&a,&b);
    printf("The sum = %d",a+b);
    return 0;
}
